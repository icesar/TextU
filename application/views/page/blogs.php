<div id="wp_sms_plugin">
	<h2>WordPress Text Message Plugin</h2>
	<img src="/images/wordpress-sms-plugin.jpg" alt="Wordpress SMS Plugin" style="float:left;margin:0 0 10px 5px;" />

	<p style="clear:both">Martin Fitzpatrick over at <a href="http://www.mutube.com">mutube</a> has developed a cool WordPress Plugin that let's you receive text messages from your blogs visitors.  It's similar to  <a href="text-me-box.php">our Text Me box</a>, except that since it installs as a plugin, there is no need for the script to be run off-site.</p>
	
	<p>Martin's plugin works not only in the US, but also in the UK, Germany, Italy, the Netherlands, Spain, Austria and Australia.  You can learn more about the <a href="http://www.mutube.com/projects/wordpress/quick-sms/" title="Quick SMS for WordPress">Quick SMS project</a> on Martin's website.</p>
	
