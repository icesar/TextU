<h2>Texting Abbreviations That Everyone Should Know</h2>

<img src="/images/texting-abbreviations.jpg" class="alignright" width="240" />
<p>No one will blame you for not understanding WTF your kids are texting the first time you sneak a peak at their phones. But for every time after that, just make sure you keep this handy little cheat sheet around to decipher those txts.  There is nothing mysterious about <em>texting abbreviations</em>, in fact, many of them are phonetic and will make sense if you just read them out loud.  For all the other text speak, we've compiled the list below, and made our best efforts to keep it down to abbreviations that people <strong>actually</strong> use.</p>

