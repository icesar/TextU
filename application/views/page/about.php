
<h2>About TextU.org</h2>

<p><span class="logotext">TextU.org</span> <em>allows you to send text messages from the internet</em> to people's mobile phones. It's simple. I built the service years ago because it was something I needed and have kept it online ever since.  Thank you for using the service, and if you have any comments or feedback, I'd love to hear from you.  I really have tried to make this the best option for sending SMS's online.  The service and hosting are supported through advertising revenue. Thanks so much for using us!</p>

<img src="/images/startac.jpg" class="alignright" title="The coveted Motorola StarTAC" width="240" />
<p>I started <span class="logotext">TextU.org</span> in the early 2000's because, well, I was too cheap to enable text messaging on my trusty Motorola StarTAC. Do you remember those?  I effing loved that phone.  Until my roommate put it in the blender and "accidentally" turned it on.  If you can believe that.  Anyway, I've since made a few more dollars and enabled text messaging on my phone, but I'm happy to leave this service up for all the other starving students, and for the revenue that it generates.  In fact, if you have any revenue optimizing suggestions, I'm all ears.</p>

<p>If you're still here, my name is Cesar and I'm a freelance developer and troublemaker based in beautiful, sunny Santa Monica, CA. I've spent five years  building web applications, websites, and just-for-fun projects and learned a lot along the way.  If you have any questions, ask away:</p>  

