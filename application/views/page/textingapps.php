<h2>Sending Free Texts From Your Phone</h2>

<p>If you have a smartphone, then you've got plenty of ways to avoid paying for text messaging. Not only that, but the text messaging apps are often much better than just txting because they allow cool things like texting with groups, better media management and no problems texting internationally. For free SMS from your iPhone or Android, check out the list below.</p>

<h3><a href="http://belugapods.com/">Beluga</a> - Group Messaging Made Easy</h3>

<p>Beluga helps groups of friends stay in touch on the go. Use it to plan a night out or just share updates and photos. It's instant, intimate and simple. Best of all, it's completely free. </p>

<h3><a href="http://www.textplus.com/">textPlus</a> - Free Messaging, Groups, and Pics</h3>

<p>Texting is how you keep up with your friends. Now, the easiest way to start chats, share pics and discover new people in conversations is with textPlus on your iPhone, iPad or iPod touch.</p>

<h3><a href="http://groupme.com/">GroupMe</a> - Your Real Life Network</h3>

<p>Start groups with the people already in your contacts. When you send a message, everyone instantly receives it - it's like a private chat room that works on any phone.</p>

<h3><a href="http://www.whatsapp.com/">WhatsApp</a> - Cross Platform Mobile Messaging</h3>

<p>WhatsApp Messenger is a cross-platform mobile messaging app which allows you to exchange messages without having to pay for SMS. WhatsApp Messenger is available for iPhone, BlackBerry, Android and Nokia and yes, those phones can all message each other!</p>

<h3><a href="http://pingchat.com/">PingChat</a> - Free Text, Picture, and Video Messaging</h3>

<p>PingChat! lets you send and receive instant messages, pictures, video, locations and voice notes with your data plan or wifi connection. Best of all, it's completely free.</p>


