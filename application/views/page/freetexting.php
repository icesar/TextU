<h3>Other Sites for Sending Text Messages</h3>

<p>I'm amazed at how many sites are out there for sending free text messages. You'd think that this is the popularest thing ever to do on the interwebs. But then you take a closer look and you realize that everyone is just using some version of a crappy script some college student wrote in the 1990s.  So while this list is here, there's not much in it that I recommend using.  Stick with <a href="http://www.skype.com">Skype</a> if you want a legit, trustworthy service.  And if you want something fast and easy, then stick with us.</p>

<ul>
	<li><a href="http://www.skype.com/">Skype</a> - The industry leader for all things communication.  Not free, and also not janky.</li>	
	<li><a href="http://www.yakedi.com/">Yakedi</a> - Recommended by Mashable, account creation required.</li>
	<li><a href="http://smspup.com/">SmsPup</a> - iPhone app also available. Account required, but looks legit.</li>
	<li><a href="http://www.sendsmsnow.com/">SendSmsNow</a> - Option to send international texts. Anyone in Ghana want to test with me?</li>
	<li><a href="http://www.txt2day.com/">Txt2Day</a> - Ooh pretty! But it doesn't hide the same old script as everyone else.</li>
	<li><a href="http://www.text4free.net/">Text4Free</a> - A full page of ads, THEN the same old script. I'm seeing a pattern.</li>
	<li><a href="http://www.textem.net/">TextEm</a> - Same old script, different design. Get your reading glasses.</li>
	<li><a href="http://www.anontxt.com/">AnonTxt</a> - Clean design, but anonymous texts (to me) means "spammers and weirdos welcome."</li>
	<li><a href="http://www.mobile-sender.com/">MobileSender</a> - This is the original version of the script. At least ads are not obstrusive.</li>
	<li><a href="http://www.onlinetextmessage.com/">OnlineTextMessage</a> - Really? A drop down list of abbreviations? Who asked for that?</li>
	<li><a href="http://www.youtextmessage.com/">YourTextMessage</a> - Allows MMS, and also requires account creation.</li>
	<li><a href="http://www.txtdrop.com/">TxtDrop</a> - These guys are great at SEO but not at sending texts. Didn't work for me. 15 million??</li>
	<li><a href="http://textingmania.com/">TextingMania</a> - This is the jankiest version of the script. Meh.</li>
	<li><a href="http://www.smsfun.com.au/">SmsFun</a> - Free online texting for Aussies, yay!</li>
</ul>

<p>Also, check out <a href="http://mashable.com/2008/04/19/send-text-messages/">Mashable on online texting</a>. Frankly, I'm a little offended that we didn't make their top 10 list. Ok, and now I'm over it.</p>